def ensure_repo(repo: Path) -> None:
    """Init the repo on `main` AND write .gitignore before any `git add -A` runs, so no use case
    ever commits node_modules/.venv/build artifacts/secrets. Idempotent — safe to call repeatedly.
    For a pre-existing repo that already tracked junk (e.g. re-run), untrack whatever the fresh
    .gitignore now excludes (files stay on disk; only the index is cleaned)."""
    fresh = not (repo / ".git").exists()
    if fresh:
        git(repo, "init", "-q"); git(repo, "checkout", "-q", "-b", "main")
    gi = repo / ".gitignore"
    cur = gi.read_text() if gi.exists() else ""
    # refresh a STALE ignore too (an existing repo created before .dart_define.json/local.properties
    # were added) so newly-ignored cred files are untracked below — not just fresh-repo creation.
    if not gi.exists() or "node_modules/" not in cur or ".dart_define.json" not in cur:
        gi.write_text(GITIGNORE)
    if not fresh:  # existing repo — drop any tracked files the .gitignore now ignores
        code, tracked = git(repo, "ls-files", "-ci", "--exclude-standard")
        if code == 0 and tracked.strip():
            sh_shell(f"cd {repo!s} && git ls-files -ci --exclude-standard -z | "
                     f"xargs -0 git rm -r --cached --quiet 2>/dev/null || true")


def sh(*args, cwd=None, timeout=600) -> tuple[int, str]:
    p = subprocess.run(list(args), cwd=cwd, text=True, timeout=timeout,
                       stdout=subprocess.PIPE, stderr=subprocess.STDOUT)
    return p.returncode, p.stdout


def sh_shell(cmd, timeout=1200) -> tuple[int, str]:
    p = subprocess.run(cmd, shell=True, text=True, timeout=timeout,
                       stdout=subprocess.PIPE, stderr=subprocess.STDOUT)
    return p.returncode, p.stdout


def augment_path(S):
    """Make self-installed tools (maestro, android) findable by later stages."""
    import shutil as _sh
    for p in S.get("bootstrap", {}).get("extra_path", []):
        pe = os.path.expanduser(p)
        if pe not in os.environ.get("PATH", ""):
            os.environ["PATH"] = pe + os.pathsep + os.environ.get("PATH", "")
    return _sh


def bootstrap_tools(S) -> dict:
    """Idempotent Phase-0 installs — only run when a tool is actually missing."""
    import shutil
    b = S.get("bootstrap", {}); res = {}
    have_maestro = shutil.which("maestro") or Path(os.path.expanduser("~/.maestro/bin/maestro")).exists()
    if not have_maestro and b.get("maestro_install"):
        code, _ = sh_shell(b["maestro_install"]); res["maestro"] = (code == 0)
    else:
        res["maestro"] = bool(have_maestro)
    # playwright browsers (global cache); the @playwright/test pkg is added per-web-project by the coder
    if b.get("playwright_install"):
        code, _ = sh_shell(b["playwright_install"]); res["playwright"] = (code == 0)
    return res


def die_gate(msg):
    print(f"GATE-FAIL: {msg}", file=sys.stderr); sys.exit(2)


def fail_gate(S, uc, stage, summary, signals, *, gate=None, err="", evidence=None):
    """Record a failure with a DETERMINISTICALLY-CLASSIFIED cause (infra/agent/harness/skills/setup)
    before halting, so a boot/harness failure is never mislabelled 'skills' and poisons the gaps
    ledger. Use in the stages where the cause actually matters (verify/integrate/boot/demo)."""
    cause = obs.record_failure(S, uc["slug"], stage, gate=gate or stage, summary=summary,
                               signals=signals, err=err, evidence=evidence)
    print(f"GATE-FAIL[{cause}]: {summary} tag={cause}", file=sys.stderr)
    sys.exit(2)


def secret_scan_or_die(S, uc, repo, stage):
    """The STEP4 §4.6 pre-push gate that was promised but never implemented: block the push if any
    real secret VALUE (from .env.pipeline) appears in a TRACKED file. Runs before every git push."""
    load_envfile_into_os(S, uc)
    scan = secrets.scan_repo(repo, str(uc_env_file(S, uc)))
    if not scan.get("clean"):
        fail_gate(S, uc, stage,
                  f"{stage}:{uc['slug']} SECRET-SCAN BLOCKED push — {scan.get('hits') or scan.get('error')}",
                  {"exit": 2}, gate="secret-scan", evidence=[str(h) for h in scan.get("hits", [])])


