def _only_filter(comps):
    """Scope a stage's component list to STEP4_ONLY (comma-separated kinds or names),
    e.g. STEP4_ONLY=web builds/integrates ONLY the web slice and leaves every other
    component (and its committed integration) untouched. Empty env -> no filtering."""
    only = {x.strip() for x in os.environ.get("STEP4_ONLY", "").split(",") if x.strip()}
    if not only:
        return comps
    kept = [c for c in comps if c["kind"] in only or c["name"] in only]
    if kept:
        print(f"  [scoped] STEP4_ONLY={sorted(only)} -> {[c['name'] for c in kept]}")
    return kept


def _spec_depth_check(text: str) -> tuple[bool, list[str]]:
    """Gate the generated spec against the DEPTH STANDARD (requirements.md.tmpl §A-E). A spec that only
    pins the fixed login→chat→call flow with a thin seed silently produces a SHALLOW app (the pipeline
    gates on compiles+boots, not depth). Lenient, synonym-tolerant markers — a spec that actually follows
    the template passes; a genuinely shallow one (no CRUD, no states, no images, no UI states) fails.
    Returns (ok, missing-markers)."""
    t = text.lower()
    checks = {
        "full CRUD (create/edit/delete in-app)": "crud" in t or all(w in t for w in ("create", "edit", "delete")),
        "entity state machine (status + transitions)":
            any(w in t for w in ("status", "state machine")) and ("→" in text or "->" in t or "transition" in t),
        "user avatars (avatar_url)": "avatar" in t,
        "entity images (image_url)": "image_url" in t or ("image" in t and "url" in t),
        "image/media chat message": any(w in t for w in ("image message", "media message", "image/media")),
        "empty/loading/error UI states": "empty" in t and "loading" in t and ("retry" in t or "error state" in t),
    }
    missing = [k for k, ok in checks.items() if not ok]
    return (not missing, missing)


def _generate_spec_with_depth(S, uc, req_file, repo, max_tries: int = 2) -> bool:
    """Generate the spec-pin and ENFORCE the DEPTH STANDARD. If the first draft is shallow, regenerate
    ONCE with the missing markers called out (self-heal), then gate. A spec that stays shallow after the
    retry halts preflight (tag=agent → codegen note, not a skills gap) so no shallow app is ever built."""
    base_prompt = prompts.render_requirements(S, uc)
    missing: list[str] = []
    for attempt in range(max_tries):
        prompt = base_prompt if attempt == 0 else (
            base_prompt + f"\n\n⚠ YOUR PREVIOUS DRAFT WAS REJECTED AS SHALLOW — it did not satisfy the DEPTH "
            f"STANDARD. It was MISSING: {', '.join(missing)}. Rewrite requirements/{uc['slug']}.md so EVERY "
            f"one of those is explicitly present (see DEPTH STANDARD A-E). Overwrite the existing file.")
        claude_runner.run_headless(prompt, settings=S, phase="A", cwd=HERE, model_key="spec",
                                   label=f"spec-{uc['slug']}" + ("" if attempt == 0 else f"-retry{attempt}"),
                                   log_dir=repo / "_logs")
        if not (req_file.exists() and req_file.stat().st_size > 200):
            continue
        ok, missing = _spec_depth_check(req_file.read_text())
        obs.event(S, uc["slug"], "preflight", "spec_depth", attempt=attempt, ok=ok, missing=missing)
        if ok:
            if attempt:
                print(f"  ✔ spec-depth: {uc['slug']} passed on retry {attempt}")
            return True
        print(f"  ⚠ spec-depth: {uc['slug']} draft {attempt + 1} SHALLOW — missing {missing}; "
              f"{'regenerating' if attempt + 1 < max_tries else 'out of retries'}")
    if req_file.exists() and req_file.stat().st_size > 200:
        die_gate(f"preflight:{uc['slug']} spec still SHALLOW after {max_tries} tries — missing {missing}; "
                 f"DEPTH STANDARD (requirements.md.tmpl §A-E) not met tag=agent")
    return False


def stage_preflight(S, uc):
    """Phase 0 — initialise + stack-readiness gate + generate the spec-pin (req:baseline).
    No codegen tokens are spent past here unless this use case's stacks are actually buildable."""
    comps = prompts.expand_components(uc)
    rd = readiness.check(uc, comps, need_docker=True)
    # self-provision the e2e toolchain (installs only what's missing) so verify isn't blocked later
    boot_tools = bootstrap_tools(S)
    # initialise workspace + git
    repo = state.repo_dir(S, uc["slug"]); repo.mkdir(parents=True, exist_ok=True)
    ensure_repo(repo)  # init on main + write .gitignore BEFORE any codegen/add can track junk
    # generate the spec-pin if missing (this is "requirements build once implementation starts")
    req_file = HERE / "requirements" / f"{uc['slug']}.md"
    spec_generated = req_file.exists()
    if rd["ready"] and not spec_generated:
        req_file.parent.mkdir(parents=True, exist_ok=True)
        # Generate + ENFORCE the DEPTH STANDARD (regenerate-once self-heal, then gate). Existing specs
        # (del/com/mkt) already exist → skipped here, so this only deepens dat forward.
        spec_generated = _generate_spec_with_depth(S, uc, req_file, repo)
    res = {"useCase": uc["name"], "slug": uc["slug"], "ready": rd["ready"], "missing": rd["missing"],
           "byComponent": rd["byComponent"], "workspaceInit": (repo / ".git").exists(),
           "e2eTools": boot_tools, "specGenerated": spec_generated, "specFile": str(req_file)}
    state.write(S, uc["slug"], "preflight", res)
    if not rd["ready"]:
        die_gate(f"preflight:{uc['slug']} not build-ready — missing {rd['missing']} tag=agent")
    print(f"OK preflight:{uc['slug']} ready; spec={'yes' if spec_generated else 'PENDING'}"); return res


def stage_provision(S, uc):
    """Provision a SEPARATE CometChat app for THIS use case (not a shared one): use the account-level
    automation/management keys to create app `step4-<slug>`, capture its APP_ID/REGION/AUTH_KEY/
    REST_API_KEY, and write them to the per-UC file runs/<slug>/.env.cometchat (+ cometchat-app-config.json).
    Idempotent per use case: if the per-UC file already names a valid app, no-op."""
    repo = state.repo_dir(S, uc["slug"]); repo.mkdir(parents=True, exist_ok=True)
    ue = uc_env_file(S, uc)
    load_envfile_into_os(S, uc)                         # global account keys (automation/APNs/FCM/webhook)
    if ue.exists() and cometchat._cfg(str(ue)).get("COMETCHAT_APP_ID"):
        res = {"appIdSet": True, "skipped": "already provisioned (per-UC app)", "appIdFile": str(ue)}
        state.write(S, uc["slug"], "provision-app", res)
        print(f"OK provision-app: {uc['slug']} already has its own app ({ue.name}) — no-op"); return res
    # Do NOT let the agent reuse a shared app: hide any shared app-specific keys — only the ACCOUNT
    # automation keys should be visible, forcing a fresh per-UC app.
    for k in ("COMETCHAT_APP_ID", "COMETCHAT_AUTH_KEY", "COMETCHAT_REST_API_KEY", "COMETCHAT_REGION"):
        os.environ.pop(k, None)
    r = claude_runner.run_headless(prompts.render_provision(S, uc), settings=S, phase="B",
                                   cwd=repo, model_key="provision", label=f"provision-{uc['slug']}",
                                   log_dir=repo / "_logs")
    app_set = ue.exists() and bool(cometchat._cfg(str(ue)).get("COMETCHAT_APP_ID"))
    res = {"appIdSet": app_set, "exitCode": r["exitCode"], "appIdFile": str(ue), "tail": r["tail"]}
    state.write(S, uc["slug"], "provision-app", res)
    if not app_set:
        die_gate(f"provision-app:{uc['slug']} — no per-UC app created at {ue.name}; check "
                 f"COMETCHAT_DASHBOARD_CHECKLIST.md; provisioning residue is a finding tag=docs-mcp")
    load_envfile_into_os(S, uc)                    # now overlay the freshly-provisioned per-UC creds
    print(f"OK provision-app: {uc['slug']} — dedicated app created + creds written to {ue.name}"); return res


def stage_build(S, uc):
    repo = state.repo_dir(S, uc["slug"]); repo.mkdir(parents=True, exist_ok=True)
    logs = repo / "_logs"
    ensure_repo(repo)  # init on main + write .gitignore BEFORE any codegen/add can track junk
    comps, worst, runs = _only_filter(prompts.expand_components(uc)), 0, []
    for c in comps:
        cdir = repo / c["dir"]
        r = claude_runner.run_headless(prompts.render_build(S, uc, c), settings=S, phase="A",
                                       cwd=cdir, model_key="build", label=f"build-{c['name']}", log_dir=logs)
        g = verify.build_gate(c["kind"], cdir)
        # SELF-HEAL: if the gate failed with a known UC1 signature, auto-repair + retry once — no human.
        if g["buildExitCode"] != 0:
            ctx = {"stage": "build", "kind": c["kind"], "stack": c["stack"],
                   "repo_dir": repo, "comp_dir": cdir, "mobile_dir": cdir,
                   "env_file": str(uc_env_file(S, uc)), "integrated": False}
            healed = selfheal.heal(ctx, g["outputTail"])
            if healed:
                print(f"  self-heal build:{c['name']} → {[h['rule'] for h in healed]}; retrying")
                # thread the heal env (e.g. JAVA_HOME=JDK17) into the re-gate — else the fix no-ops
                g = verify.build_gate(c["kind"], cdir, env=ctx.get("env")); g["selfHealed"] = [h["rule"] for h in healed]
        runs.append({**c, **r, **g}); worst = max(worst, g["buildExitCode"])  # compile authoritative; agentOk gates truncation
    sha = ""
    if worst == 0:
        git(repo, "add", "-A"); git(repo, "commit", "-q", "-m", f"baseline {uc['name']} (no CometChat)")
        _, sha = git(repo, "rev-parse", "HEAD"); sha = sha.strip()
    res = {"useCase": uc["name"], "slug": uc["slug"], "stack": uc["archetype"],
           "buildExitCode": worst, "committedSha": sha,
           "components": runs, "outputTail": (runs[-1]["outputTail"] if runs else "")}
    state.write(S, uc["slug"], "build", res)
    if not gates.baseline(res):
        die_gate(f"baseline:{uc['slug']} (buildExit={worst}, sha={'yes' if sha else 'no'}) tag=agent")
    print(f"OK build:{uc['slug']} sha={sha[:9]}"); return res


