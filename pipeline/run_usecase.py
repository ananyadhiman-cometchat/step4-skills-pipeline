#!/usr/bin/env python3
"""run_usecase.py — deterministic stage worker for the STEP4 hybrid pipeline.

One stage per invocation; exits after each. Holds NO state between calls — all
state is on disk (git repo in work_root/<slug>/, JSON in _reports/ + pipeline-state/).
The ONLY LLM calls are the per-component `claude -p` codegen + one judge; every
gate is deterministic Python. Never blocks on input() — the conductor (session or
batch_runner.py) sequences stages and holds the human checkpoints.

Stages:  build → boot → push-main → integrate → verify → push-branch
Exit:    0 ok+gate-pass · 2 gate-fail (conductor halts) · 3 stage error

Usage:   python3 run_usecase.py --use-case mkt --stage build
"""
